# String Operations

# Concatenation
full_name = "Jaideep" + " " + "Bhagat"
print("Concatenation: ", full_name)

# Repetition
repeated_string = "Hello! " * 3
print("Repetition: ", repeated_string)

# Length of a String
length = len("Hello, Jaideep")
print("Length: ", length)

# Accessing characters (indexing)
first_char = "Hello"[0]
print("First Character: ", first_char)

# Slicing Strings
sub_string = "Hello, Jaideep"[0:5]
print("Slicing: ", sub_string)

# Converting into upper and lower case
upper_case = "Hello".upper()
print("Upper case: ", upper_case)

lower_case = "HELLO".lower()
print("Lower Case: ", lower_case)

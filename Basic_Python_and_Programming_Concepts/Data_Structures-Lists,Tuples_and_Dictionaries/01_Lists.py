# Creating a to-do list of daily tasks for a student
todo_list = ["Complete Assignment", "Read Python Documentation", "Attend Python Class"]

# Printing the list
print(todo_list)

# Adding a new task
todo_list.append("Submit Assignment")
print(todo_list)

# Removing a task
todo_list.remove("Read Python Documentation")
print(todo_list)

# Replacing a task
todo_list[0] = "Read Chapter 1"
print(todo_list)

# Length of the list
print(len(todo_list))

# Sorting the list
todo_list.sort()
print(todo_list)

# Reversing the list
todo_list.reverse()
print(todo_list)

# Clearing the list
todo_list.clear()
print(todo_list)

# Length of the list
print(len(todo_list))

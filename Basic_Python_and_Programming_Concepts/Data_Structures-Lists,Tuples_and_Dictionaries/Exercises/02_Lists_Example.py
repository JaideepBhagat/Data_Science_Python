fruits = ['apple', 'banana', 'cherry']
print(fruits)  # Output: ['apple', 'banana', 'cherry']

# Adding an item
fruits.append('date')
print(fruits)  # Output: ['apple', 'banana', 'cherry', 'date']

# Modifying an item
fruits[1] = 'blueberry'
print(fruits)  # Output: ['apple', 'blueberry', 'cherry', 'date']

# Removing an item
fruits.remove('apple')
print(fruits)  # Output: ['blueberry', 'cherry', 'date']
